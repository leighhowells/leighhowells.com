                <h1>The significance of the collection</h1>
                
              <p>As Professor Andrew Barker from the School of Literatures, Languages and Cultures explains, the collection presents a complete overview of the GDR’s literary (and sometimes broader) culture from its inception to its end (1949 to 1989), with the main focus on the period from 1960 to 1989.</p>
                
                <p>“The collection gives an in-depth view of a literary culture that was state-sponsored, and whose vibrancy came from the fact that state-sponsored artists were often being subversive within literature,” explains Professor Barker. “There is this very vibrant relationship between literature, the politics of the day and the readership.”</p>
                
                <h3>A coveted research tool</h3>
                
                <p>Containing works from both internationally renowned and lesser-known GDR writers, including a range of female authors, the collection boasts a quality and quantity that make it a most coveted research tool.</p>
                
                <p>“It is rare, particularly in this country, to have access to a collection of this magnitude,” explains Professor Barker. “It covers a gamut of literature, from narrative writing, novels and short stories to dramatic pieces, travel articles, periodicals and criticisms. It consists almost entirely of first editions, nearly all in their dust jackets, gathered together by a very, very competent collector who really knew the scene inside out.”</p>
                
                <p>The collection has inherent aesthetic qualities as well as immense historical interest and, according to Professor Barker, students choosing to examine it will benefit from first-rate supervision - specialists Dr Laura Bradley and Dr Peter Davies are already working with the material and can supervise research on a wide range of related topics, and GDR experts Dr Pertti Ahonen (History) and Dr Elaine Kelly (Music) can cater for postgraduates whose interests cross inter-disciplinary boundaries.</p>
                
                <p>“This collection is for those postgraduates aiming to understand literature and dictatorship, media control, lack of freedom of the artist within a totalitarian system, how far art can be directed, the function of literature in a closed society…” says Professor Barker.</p>
                
                
                
                
                
                
                
