          <h1>About us</h1>
            
          <p>German is one of the larger modern language subject areas with around 250 students, six full-time members of staff, one part-time language tutor, and one German LektorIn. Several members of staff are native speakers of German.</p>
            
            <p>We offer a variety of undergraduate degree programmes, both single and joint honours, and have a lively postgraduate community studying for a broad range of taught Masters, Masters by Research and PhD degrees.</p>
           
        <hr>
        <h2>Facilities</h2>
            
        <p>The University possesses a large library with good stocks in most aspects of German studies. German itself offers the Gibson Library with its collection of many primary and secondary texts, together with reference works.</p>
            
        <p>The city of Edinburgh is also home to the National Library of Scotland, one of the largest and best libraries in the UK, with superb German holdings.</p>
            
            <p><a href="#" class="btn btn-default btn-sm"> National Libary of Scotland <span class="glyphicon glyphicon-chevron-right"></span></a></p>
            
        <p>German at Edinburgh makes increasing use of e-learning facilities, via the universitys WebCT platform which supports many aspects of teaching and learning.</p>

            <p>All students have access to satellite TV and film viewing facilities in the Language and Humanities Centre and excellent computing facilities.</p>
            
<p><a href="#" class="btn btn-default btn-sm"> Languages and Humanities Centre <span class="glyphicon glyphicon-chevron-right"></span></a></p>
 
<hr>
<h2>Research</h2>
            
<p>Since our success at the 2008 RAE (50% of our research research being considered world-leading or internationally excellent) research at Edinburgh has continued to expand into new areas. This is reflected in a very broad range of topics such as German-Jewish writing; German literature and mental health as well as disabilities studies; Holocaust writing and translation; theatre, film and censorship; travel/migration writing and cultural exchange; gender and masculinity studies. Our innovative Edinburgh German Yearbook, planned and edited within the section and published by Camden House in the USA, attracts high-profile academic contributors with their cutting-edge research.</p>
            
            
<p>We have recently acquired the Karin McPherson collection of GDR literature, consisting of some 1000 volumes and accessible for research purposes.</p>
            
<p>Our regular research seminars feature papers presented by Edinburgh staff and students as well as invited speakers from Britain and abroad. Colleagues are increasingly involved in activities that foster knowledge exchange between academia and the public. The film series ‘The Stasi Are Among Us’ featured at the Glasgow Film Festival and was accompanied by discussions with East German filmmakers on working under socialism in the GDR. The one-day symposium ‘Making a Difference’ at the Scottish Mental Health Arts and Film Festival in Edinburgh brought together German, Scottish and Irish filmmakers, writers and scholars as well as psychiatrists and psychotherapists and led to fruitful exchange between these disciplines.</p>

<hr>
            
<h2>Beyond the curriculum</h2>
<h3>The German play and other extra-curricular activities</h3>
            
<p>One of the high points in the extra-curricular life of German at Edinburgh is the annual play. Recent productions include plays by Tankred Dorst, Elfride Jelinek and Bertolt Brecht. In 2006 we collaborated with the German playwright Kristo Sagor on the UK premiere of Hautkopf. In 2010 our writer-in-residence Christiane Rösinger took part in a number of tutorials and seminars, and worked with students to turn her autobiography Das schöne Leben into a play. Christiane also attracted a large audience at her public concert and reading.</p>
            
<p>The LektorIn organises regular film nights followed by a Stammtisch. Our language tutor has organised tours of exhibitions of German artists at Edinburgh’s National Gallery in German. A study trip to Munich in 2011 gave interested students the opportunity to learn more about German culture first-hand.</p>
            
<h3>The German society</h3>

<p>The German Society (run by the students) engages in a wide variety of activities, ranging from a Bierfest to a trip to Berlin.</p>
            
<h3>Goethe-Institut</h3>
            
<p>There are also close links with the Scottish Branch of the Goethe-Institut in Glasgow, while the Edinburgh German Circle offers a varied programme of events relating to the life and culture of the German-speaking peoples.</p>
            
<p><a href="#" class="btn btn-default btn-sm"> Goethe-Institut in Glasgow <span class="glyphicon glyphicon-chevron-right"></span></a></p>
            
<h3>German native speakers</h3>
            
<p>There is a sizeable community of native speakers of German in Edinburgh whose (extremely ecumenical) church provides a further focus of interest.</p>
            
<p><a href="#" class="btn btn-default btn-sm"> German-speaking Congregation of Edinburgh <span class="glyphicon glyphicon-chevron-right"></span></a></p>
            
