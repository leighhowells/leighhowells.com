            <h4>AWID Key Actions</h4>

            <p style="font-size: 1em;">AWID’s Challenging Religious Fundamentalisms (CF) initiative was launched in 2007. It seeks to contribute to greater strategic thinking, dialogue and advocacy among women’s rights organizations, movements, and allies to counter religious fundamentalisms. </p>

            <p style="font-size: 1em;">We work across-regions and religions and involve a range of women’s rights actors and allies within human rights, development organizations and other progressive sectors to provide spaces to challenge fundamentalisms and strengthen advocacy initiatives in this area. </p>

                  <ul>
                    <li>Producing knowledge and information on religious fundamentalisms and women’s rights</li>
                    <li>Organizing strategic convenings to build shared understandings of the issues, enhance capacities and advance dialogue among key actors, and encourage strategizing across religions/regions/sectors to strengthen responses to religious fundamentalisms</li>
                    <li>Contributing to efforts to confront religious fundamentalisms through advocacy and strategic partnerships </li>
                  </ul>
