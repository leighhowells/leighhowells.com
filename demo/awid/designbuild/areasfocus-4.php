    	<h3>About this Initiative</h3>

    	<div class="col-2-3">

    	<p class="intro">Religious fundamentalisms (RFs) continue to gather strength around the world and across religions, with negative consequences for women’s rights, minority rights as well as human rights in general.</p>
      <p> Fundamentalist tendencies often use religion to mask political interests and agendas, assert social control, reinforce patriarchal structures, and gain access to power and resources. In many contexts, socio-economic control and the imposition of extreme religious views is exercised through the use of violence. </p>
 
	<p>Fundamentalisms are characterized by their absolutism, their intolerance and suppression of dissent, their control of women’s bodies and sexuality, and imposition of a single worldview. Increasingly, fundamentalist forces have a strong influence in international fora where they have had some success in invoking religion, culture, nationalism and tradition to stall and claw back progress on critical women’s rights issues and provoke political deadlock. </p>

<p>Despite these challenges, women’s rights activists and other progressive rights-based actors are continuing to respond and resist religious fundamentalisms as well as build proactive strategies to advance rights.</p>


