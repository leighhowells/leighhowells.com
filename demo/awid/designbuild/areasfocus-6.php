     <h1 style="margin:0;padding:0;border-bottom:none;">Challenging Religious Fundamentalisms</h1>

      <p class="intro">Religious fundamentalisms (RFs) continue to gather strength around the world and across religions, with negative consequences for women’s rights, minority rights as well as human rights in general.</p>
      <p> Fundamentalist tendencies often use religion to mask political interests and agendas, assert social control, reinforce patriarchal structures, and gain access to power and resources. In many contexts, socio-economic control and the imposition of extreme religious views is exercised through the use of violence. </p>
 
     <aside>
                <blockquote style="height: 260px;"> 
                <div class="col-1-4"><span class="icon icon-issue1" style="font-size: 3.5em; color: #ccc;"></span></div>

