      <h1>The role of your website in raising awareness</h1>

      <p style="font-weight: 300; font-size: 1em;  padding-top: 40px; letter-spacing: 1.3px;">By Paul Boag</p>


      <p class="intro">We have recently had two clients insist that their websites feature social media icons. Whenever this happens it always makes me nervous. Nervous because these icons can distract from more important primary actions.</p>

<p>But in at least one of these two cases, things were different. Raising awareness and social sharing <strong>was</strong> their primary action. They wanted to encourage people to share their content above anything else. This left me with a dilemma, were social media icons the right approach for them or was their a better way?
</p>

<h3 id="myproblemwithsocialmediasharingicons">My problem with social media sharing icons</h3>
<p>Despite social sharing being the primary call to action, I still didn’t feel that social media icons were the way to go. I felt like this for many reasons:
</p>
<ul>
  <li>They have a significant impact on performance.</li>
  <li><a href="https://insidegovuk.blog.gov.uk/2014/02/20/gov-uk-social-sharing-buttons-the-first-10-weeks/">Some research indicates they are rarely used</a>.</li>
  <li>They don’t answer the question “Why should I share this?”</li>
  <li>They don’t provide guidance to the user about what is worth sharing.</li>
  <li>Content with zero shares looks unpopular.</li>
</ul>
<aside class="bob tweetable">

<blockquote>Slapping a few icons on your site gives is a false sense that you have solved social sharing.</blockquote>
</aside>
<p>
Put another way, they are a lazy solution. By slapping a few icons on your site there is a false sense that you have solved social sharing. In fact social sharing begins and ends on social networks and is all about engaging content. It is about going and engaging with people where they are.
</p>

<h3 id="gotothepeople.dontexpectthemtocometoyou.">Go to the people. Don’t expect them to come to you.</h3>

<p>When you have a message to spread there are two ways of approaching things.</p>

<p>The first approach is to try and draw people to you so they can hear your message. This is the website centric approach to digital. It says “how can we get people to come and view this content?” It encourages social sharing with the hopes that those who see those shares will click through. The emphasis is on drawing people in.
</p>
<p>
This approach makes a lot of sense when you want a user to take a particular action that can only happen on the website. An action such as making a donation or purchasing a product. But when it comes to building awareness, this approach falls down.
</p>

<aside class="bob tweetable">
<blockquote>If awareness building is your primary goal then the website is only ever going to fulfil a supporting function.</blockquote>
</aside>

<p>The second approach is to go to where people are and take the message to them. The ice bucket challenge is a great example of that. Awareness for Motor Neurone disease has skyrocketed without anybody ever visiting the website.
</p>
<p>
In short, if awareness building is your primary goal then the website is only ever going to fulfil a supporting function.
</p>

<h3 id="thesupportingroleofthewebsite">The supporting role of the website</h3>
<p>
So what is the supporting role of the website in awareness building? I think it falls into two areas:
</p>
<ul>
  <li>Plugging the weaknesses of social media channels.</li>
  <li>Drawing attention to activity on social media platforms.</li>
</ul>
<p>
Let’s look at each in turn.
</p>

<h4 id="pluggingtheweaknessesofsocialmediachannels">Plugging the weaknesses of social media channels</h4>

<p>Every social media platform has its strengths and weaknesses. The website can help plug the weaknesses where they exist.
</p>
<p>
For example YouTube is a terrible place to have a conversation. The comments there almost always degrade into juvenile rubbish. If you wish to use video to spark a discussion, post those YouTube videos to your own site and host the discussion there.
</p>
<p>
Twitter is another platform with an obvious weakness — You only have 140 characters. Your website can help plug this weakness by the use of blog posts that are then shared on Twitter.
</p>
<p>
So it is not that the website has no role to play. It is just that when it comes to awareness building, social media should be driving the direction. A website should have a regularly updated, high quality blog. One with vibrant commenting to support social media discussions. Social media shouldn’t exist to drive people to your blog.
</p>

<h4 id="drawingattentiontosocialmediaplatforms">Drawing attention to social media platforms</h4>

<p>When it comes to awareness building and discussion, your site should drive users to social media platforms.</p>

<p>But again, social media icons are not the answer. Posting an icon on every page of your site that says follow us on Facebook is not enough. You need to pick your moment and tell the user why they should follow you. What will they get from following you on Facebook or Twitter? You need to give them a taste.
</p>

<aside class="bob tweetable">
<blockquote>Posting an icon on every page of your site that says follow us on Facebook is not enough.</blockquote>
</aside>

<p>That doesn’t mean posting your entire Twitter or Facebook stream to your homepage. If you are doing social media right, that will just be a stream of one sided conversations.
</p>
<p>
Instead you need to tell the user why it’s worth following and show them edited highlights. One great example I saw once was on a charity website. On their homepage they had a recent activities box. In that box they had posts like:</p>
<ul>
  <li>John Smith has just signed our petition.</li>
  <li>Marcus Lillington has posted a photo of himself raising money on a fun run.</li>
  <li>Jane Doe has accepted our charity challenge.</li>
</ul>
<p>
This box was great for a couple of reasons. One it gave people a taste of what was happening on their social media channels. Two it made people feel there was a community worth being a part of.</p>

<p>This kind of approach is so much more engaging than a follow us button.</p>

<p>That said, there are still times when you just want to encourage people to share a blog post. What do you do then if social media icons are not the answer?</p>

<h3 id="butwhataboutencouragingsharing">But what about encouraging sharing?</h3>
<p>
Truthfully, the interface and design of a website can do little to encourage sharing. You can have the world’s most fancy sharing button and it will mean nothing if the content is not there. It is content that decides whether a user shares or not.
</p>
<p>
Sure <a href="http://boagworld.com/talks/social-media-is-a-user-experience-issue/">there are little things you can do that may help</a>. The pull out quotes you can tweet in this post are examples of that. They give some suggestions to users about what might be worth sharing, but in the end people make up their own minds.
</p>
<p>
What you need is an engaging article, interesting stat, well designed infographic or a snappy video. This is what encourages sharing. That and a strong call to action tailored to the content.
</p>
<p>
This means you have to have good content creators dedicated to the role. People who understand your audience and can create a continual stream of engaging content. People who are also building a community of passionate advocates on social media platforms.
</p>
<p>
The trouble is that sounds like a lot of hard work. It’s an ongoing expense and a lot more challenging than a few social media icons. It involves worrying about <a href="http://headscape.co.uk/digital-transformation/">digital strategy and governance</a>. It may need changes in the way you operate as an organisation. Unfortunately that is the only thing that will get the job done.
</p>
<p>
But once you have built and nurtured your community, then the website comes into its own.
</p>
<h3 id="goingfromawarenesstoaction">Going from awareness to action</h3>
<p>
Where your website will excel and where design matters most is in moving people from awareness to action. Nothing beats a website for communicating a need and taking the effort out of responding to that need. It is an amazing conversion tool, but there are better tools if you want to build awareness. As always, use the right tool for the job.</p>

