    	<h3>About this Initiative</h3>

    	<div class="col-1-2">

    	<p class="intro">Religious fundamentalisms are on the rise around the world and across religions, with negative consequences for women’s 
    	rights. AWID launched the CF initiative in 2007 to strengthen the responses of women’s rights activists to this growing phenomenon.</p>
 
		<p>The CF team conducts innovative research to contribute to greater strategic thinking, dialogue and advocacy on religious fundamentalisms by women’s rights groups.  The research has identified the key characteristics of religious fundamentalisms, examined the factors that help them grow, and detailed how they impact women’s rights across regions and religions.</p>
		 
		<p>The initiative offers analysis that unmasks and demystifies religious fundamentalisms, and shares strategies that women’s rights activists use to challenge them.</p>

    <p>The CF team also supports advocacy, builds alliances with women’s rights, human rights and development organizations and facilitates strategic dialogue on religious fundamentalisms.</p>

