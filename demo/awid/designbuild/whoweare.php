<h3>Mission</h3>
<p>To be a driving force within the global community of feminist and women’s rights activists, organizations and movements, strengthening our collective voice, impact and influence to transform structures of power and decision-making and advance human rights, gender justice and environmental sustainability.</p>

 </blockquote>



<h3>Our values: the principles that guide our work</h3>

<p>Our work is built upon feminist values toward the full realization of women’s rights and human rights of all people. This includes work towards the eradication of all discriminations based on gender, sexuality, religion, age, ability, ethnicity, language, nationality, class or other factors. We are committed to:</p>

<ul>
<li>Work as part of a movement to build our collective voice, power and influence</li>
<li>Diversity and inclusion, ensuring that in our work we are opening spaces and convening different expressions of women’s organizing—particularly from groups that have not been as visible or included in more traditional women’s rights work and instances in the past</li>
<li>Transparency, responsible use of financial resources, fairness, accountability and integrity in all our interactions</li>
<li>Work independently from any political party, government or religious institution</li>
<li>Strive for excellence, while being creative, bold and courageous</li>
</ul>


