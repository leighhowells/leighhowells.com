<h3>Our outcomes: what we hope to achieve</h3>

<p>In working with diverse partners and allies and our broader constituency AWID seeks to achieve the following five outcomes by the end of our Strategic Plan.</p>

<ol class="boxes-list">

<li>Women’s rights organizations and movements are well informed and equipped to effectively respond to opportunities and counter threats to women’s human rights at different levels. </li>

<li>Shifts made in policies and practices of diverse influential actors and institutions that shape or influence human rights and development outcomes (including donors, development agencies, business sector, human rights institutions and major CSOs) to advance, respect, protect and fulfill women’s rights and promote gender justice.</li>

<li>Expanded solidarity and powerful joint action among women’s rights activists and allies through meaningful inclusion and engagement of historically excluded or discriminated groups of women.</li>

<li>Women’s rights activists across generations, causes and movements are accessing and using knowledge and other resources to strengthen their security and wellness, and expand the enjoyment of their own human rights.</li>

<li>Greater visibility and understanding of women’s rights initiatives, the threats they face and effective response strategies led by women and women’s rights organizations, among influential actors and institutions that shape human rights and development outcomes.</li>

</ol>
