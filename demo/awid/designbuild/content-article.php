                <h1>Patriarchy Allows Child Marriage And Female Genital Mutilation To Flourish</h1>

                <p style="font-weight: 300; font-size: 1em;  padding-top: 40px; letter-spacing: 1.3px;">By Ghadeer Malek, Rachel Arinii and Nelly Bassily</p>

                <p class="intro">FRIDAY FILES - This week's blog post written by our Young Feminist Activism team on the recent Girl Summit in London originally published on the Guardian's Poverty Matters blog.</p>




<p>Young feminists must help steer the fight against wider issues harming girls including poverty, marginalisation and exclusion</p>

<p>
That is a lot of pressure to put on the shoulders of girls. This raises the question: why, with all this interest in girls, are their rights violated in every country?</p>



<p>The Girl Summit in London, UK, this week has generated momentum and opportunities for funding to end child, early and forced marriage and female genital mutilation (FGM). While that is commendable, if we look beyond the pledges and outcome of the summit, it is evident that these harmful practices are connected to other injustices girls endure.</p>

<p>Statistics show 30 million girls are at risk of FGM in the next decade, and, each year, about 14 million girls are forced to marry before they are ready. The Universal Declaration of Human Rights and the UN convention on the rights of the child should prevent such injustices, yet girls' basic rights to health, education and security remain unmet. As young feminists, we know that patriarchy perpetuates the idea that girls are of less value, which leads to their systematic neglect in economic, political, social, legal and educational realms.</p>

          

<p>Forcibly removing part of a girl's vagina is a way to control her sexuality, her right to choice and her right to freedom. FGM tends to happen with the complicity of families, communities and police, who not only do not report the crime, but often try to hide it. Patriarchy allows them to do this with impunity.</p>

<p>The commitments made at the Girl Summit on eradicating FGM and child marriage, the focus on tougher laws (including putting the onus on parents to protect girls from FGM) and increased funding for prevention programmes are important steps to combat these harmful practices. But until we link these issues to girls' lack of education, poverty, marginalisation and exclusion in the patriarchal societies in which they live, little will change.</p>

<p>While girls can and should speak for themselves, they cannot achieve these societal changes alone. As young feminists, we believe every girl has the right to control her body and determine her future. Young feminists and rights advocates play a key role in articulating girls' needs, raising awareness of rights, educating their families and communities, and advocating change at local, national, regional and global levels.</p>

<p>Young advocates of sexual and reproductive health and rights have been pushing at the highest levels of government and at the UN for the inclusion of comprehensive sex education in schools. We believe this is how you start to equip girls with the tools to challenge traditional gender roles and uphold their rights.</p>

<p>While it is important to lobby governments and hold them accountable through UN processes such as the creating targets to replace the millennium development goals when they expire next year, the fact that governments are signing up to a document will not in itself guarantee girls' rights. The shift from policy to individuals, families and communities respecting girls' rights must also be led by young feminists and rights advocates, in alliance with others.</p>

<p>Saturday 11 October will mark the third international day of the girl. We, and other young feminists, will use that opportunity to reiterate that all forms of oppression of girls are interconnected and to reaffirm their human rights.</p>


