                <p class="intro" style="padding:0;">The second report in a new research series on resources for women's rights organizing from the Association for Women's Rights in Development, this piece analyzes the aggregate impact of women’s organizations around the world that received grants from the Dutch Ministry of Foreign Affairs’ MDG3 Fund. Including results from 78% of the grantees, this analysis demonstrates the huge reach and transformation that is possible when organizations working to build women’s collective power for change receive serious resources for an extended period of time, with flexibility to refine their strategies to adapt to shifting contexts.</p>

