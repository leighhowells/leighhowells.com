<p>Fund-supported initiatives reached 165 countries in seven continents/ sub-continents and 15 regions of the world;</p>
<p>
  In these diverse locations, 224,773,550 people gained a new awareness of women’s rights, including new messages and analyses about the roots of gender inequality and gender-based violence, and an understanding that advancing gender equality is everyone’s responsibility, as well as an essential component of creating more just and equitable societies;</li>
</p>




